'use client';
import { useState } from 'react';
import { useRouter } from 'next/navigation';

export default function NewCasePage() {
  const [caseNumber, setCaseNumber] = useState('');
  const [title, setTitle] = useState('');
  const [narrativeReport, setNarrativeReport] = useState('');
  const router = useRouter();

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    const response = await fetch('/api/cases', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        title,
        narrative_report: narrativeReport,
        // case_number is auto-generated by the backend
      }),
    });

    if (response.ok) {
      const newCase = await response.json();
      router.push(`/cases/${newCase.case_id}`);
    } else {
      alert('Failed to create case. Please try again.');
    }
  };

  return (
    <div className="container mx-auto p-4">
      <h1 className="text-2xl font-bold mb-4">New Case</h1>
      <form onSubmit={handleSubmit} className="space-y-4">
        <div>
          <label htmlFor="title" className="block text-sm font-medium text-gray-700">Title</label>
          <input
            type="text"
            id="title"
            value={title}
            onChange={(e) => setTitle(e.target.value)}
            required
            className="mt-1 block w-full rounded-md border-gray-300 shadow-sm focus:border-indigo-500 focus:ring-indigo-500 sm:text-sm p-2"
          />
        </div>
        <div>
          <label htmlFor="narrative_report" className="block text-sm font-medium text-gray-700">Narrative Report</label>
          <textarea
            id="narrative_report"
            value={narrativeReport}
            onChange={(e) => setNarrativeReport(e.target.value)}
            rows={4}
            className="mt-1 block w-full rounded-md border-gray-300 shadow-sm focus:border-indigo-500 focus:ring-indigo-500 sm:text-sm p-2"
          />
        </div>
        <button
          type="submit"
          className="bg-green-500 hover:bg-green-700 text-white font-bold py-2 px-4 rounded"
        >
          Create Case
        </button>
      </form>
    </div>
  );
}
